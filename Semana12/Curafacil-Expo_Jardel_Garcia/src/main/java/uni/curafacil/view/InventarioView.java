    private void initForm(){
        empleado = (EmpleadoDto) Session.get("USUARIO");
        String titulo = "CONTROL DE INVENTARIO - "+ empleado.getNombre() + " / " + empleado.getCargo();
        this.setTitle(titulo);
    }
    private void cargarComboCodigos() {
        cboCodigo.removeAllItems();
        List<ComboDto> codigos = comboController.getCodigos();
        UtilView.llenarCombo(cboCodigo, codigos);
    }

    private void cargarComboCursos() {
        cboMedicamento.removeAllItems();
        List<ComboDto> medicamentos = comboController.getMedicamentos();
        UtilView.llenarCombo(cboMedicamento, medicamentos);
    }

    private void cargarProductos() {
        listaProductos = productosControler.getProductos();
        UtilView.limpiarTabla(tblDetalle);
        productosControler.llenarProductos(tblDetalle, listaProductos);
    }


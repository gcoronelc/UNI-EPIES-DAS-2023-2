    private void cboCodigoActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if (!updating) {
            updating = true;
            int index = cboCodigo.getSelectedIndex();
            if (index != 0) {
                cboMedicamento.setSelectedIndex(index);
            }
            updating = false;
        }
    }                                         

    private void cboMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {                                               
        if (!updating) {
            updating = true;
            int index = cboMedicamento.getSelectedIndex();
            if (index != 0) {
                cboCodigo.setSelectedIndex(index);
            }
            updating = false;
        }
    }


